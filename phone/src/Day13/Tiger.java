package Day13;

public class Tiger extends Animal {
	@Override
	void sound() {
		System.out.println("The Sound of the Tiger is Roar");
	}
	@Override
	void type() {
		System.out.println("The type of the Tiger is Wild Animal");
	}
	@Override
	void color() {
		System.out.println("The color of the Tiger is White ");
	}
}
