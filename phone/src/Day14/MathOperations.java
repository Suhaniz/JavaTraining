package Day14;

//Class with overloaded methods
public class MathOperations {
 
 // Add 2 integers
 public int add(int a, int b) {
     return a + b;
 }

 // Add 2 doubles
 public double add(double a, double b) {
     return a + b;
 }

 // Add 3 integers
 public int add(int a, int b, int c) {
     return a + b + c;
 }
}
