package Day2;

public class DataTypes {
 public static void main(String[] args) {
	
	//Integer Types
	byte byteVal = 100;
	short shortVal = 30000;
	int intVal = 100000;
	long longVal = 10000000000L;
	
	//Floating point types
	float floatVal = 5.75f;
	double doubleVal = 19.99;
	
	//Character type
	char charVal = 'A';
	
	//Boolean type
	boolean boolVal = true;
	
	//String (not a primitive, but commonly used)
	String strVal = "Hello, Java!";
	
	//Output all values
	System.out.println("Byte Value:" + byteVal);
	System.out.println("Short Value:" + shortVal);
	System.out.println("Integer Value:" + intVal);
	System.out.println("Long Value:" + longVal);
	System.out.println("Float Value:" + floatVal);
	System.out.println("Double Value:" + doubleVal);
	System.out.println("Character Value:" + charVal);
	System.out.println("Boolean Value:" + boolVal);
	System.out.println("String Value:" + strVal);
  }
}
